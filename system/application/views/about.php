<h4>What does this web site do?</h4>
<p>It gives details of product recalls issued by manufacturers and allows users to stay up to date with new recall notices through email alerts and rss feeds.</p>

<h4>Where does the data come from?</h4>
<p>We use data published by the <a href="http://ec.europa.eu/consumers/dyna/rapex/rapex_archives_en.cfm">European Commission</a> on their Consumer Affairs, RAPEX web pages</a>.</p>

<h4>If there's already a product recalls web site, why did you build this one?</h4>
<p>We built it to demonstrate other methods of making the data accessible to consumers. We included an email alerts facility & rss capability as well as a keyword search.</p>

<h4>I've signed up for email alerts, how do I unsubscribe?</h4>
<p>At the bottom of each email you receive there is a link to unsubscribe from that alert. Just click on it and we'll delete your alert. You can re-register for alerts at any time.</p>

<h4>If I signup for an alert, what do I receive?</h4>
<p>You will receive an email from us either weekly or monthly (depending on the option you select) containing the details of all recalls made since we last sent you a message.  An example of the email is available <a href="<?php echo site_url("about/example"); ?>" title="Example of the emails sent">here</a>.</p>

<h4>Can I help?</h4>
<p>You certainly can. The European Commission data is by no means exhaustive - there other sources of recalls that are not currently included in the set. We welcome any offers to write screen scrapers to search these alternative sources for inclusion on this site. </p>

<h4>Are you going to add more data to this service?</h4>
<p>We are currently working with the Trading Standards Institute to explore how we can best open up more regulatory information for consumers.</p>

<h4>What is Consumer Focus Labs?</h4>
<p>Consumer Focus Labs builds online tools to make peoples' dealings with companies fairer, save them money, or keep them informed about products or issues that are important to them. We're very interested in receiving ideas for useful projects from as many people as possible. Please visit our main site at <a href="http://www.consumerfocuslabs.org">http://www.consumerfocuslabs.org</a> for further information and to submit your ideas.</p>

